package com.project.nuerp.attRecycler;

public class att {
    String subject;
    String attendence;

    public att(String subject, String attendence) {
        this.subject = subject;
        this.attendence = attendence;
    }

    public String getSubject() {
        return subject;
    }

    public String getAttendence() {
        return attendence;
    }
}
